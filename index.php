			<h4 id="todo">What to do in Maine when you're here (from South to North)</h4>


			  <ul class="collection with-header" style="width: 600px">
				<li class="collection-header"><h4>Kennebunkport</h4></li>
				<li class="collection-item"><div>Summer home of the Bush family, filled with shops in a quaint downtown and multiple beautiful beaches</div></li>
			  </ul>

			  
			  <ul class="collection with-header" style="width: 600px">
				<li class="collection-header"><h4>Scarborough/Old Orchard Beach</h4></li>
				<li class="collection-item"><div>Kelsey grew up near <a href="https://visitmaine.com/things-to-do/outdoors-adventure/parks-recreation-areas/pine-point-beach/" target="_blank">Pine Point Beach</a>. which is excellent for walking, sunbathing and swimming and connects to <a href="https://www.tripadvisor.com/Attraction_Review-g40792-d504866-Reviews-Old_Orchard_Beach-Old_Orchard_Beach_Maine.html" target="_blank">Old Orchard Beach (OOB)</a>. OOB has a lively tourist scene with a pier and carnival rides.</div></li>
				<li class="collection-item"><div>Scarborough was the home of artist <a href="http://www.metmuseum.org/toah/images/h5/h5_10.64.5.jpg" target="blank">Winslow Homer</a>; you can tour his studio through the <a href="http://www.portlandmuseum.org/homer" target="_blank">Portland Museum of Art</a></div></li>
			  </ul>			  


			  <ul class="collection with-header" style="width: 600px">
				<li class="collection-header"><h4>Portland</h4></li>
				<li class="collection-item"><div>From the New York Times: <a href="http://www.nytimes.com/interactive/2016/07/28/travel/what-to-do-36-hours-portland-me.html" target="_blank">36 Hours in Portland</a></div></li>
				<li class="collection-item"><div>Portland Head Light: The most photographed <a href="https://portlandheadlight.com/" target="blank">lighthouse</a> in North America. Nearby Two Lights Lobster Shack provides a particulary scenic lobster roll experience.</div></li>
				<li class="collection-item"><div>Mackworth Island: a quick detour between Portland and Bath where a brief walk around the island provides beautiful views of Portland and Casco Bay.</div></li>
			  </ul>	  
			
			  <ul class="collection with-header" style="width: 600px">
				<li class="collection-header"><h4>Brunswick</h4></li>
				<li class="collection-item"><div>Home of <a href="https://upload.wikimedia.org/wikipedia/commons/6/66/Bowdoin_College_Quad.jpg" target="_blank">Bowdoin College</a>: scenic small town with a Maine Street (yes that's how it's spelled) and a lovely college campus with great art museum.</div></li>
			  </ul>
			  
			  <ul class="collection with-header" style="width: 600px">
				<li class="collection-header"><h4>Other highlights of Midcoast Maine</h4></li>
				<li class="collection-item"><div><a href="https://s-media-cache-ak0.pinimg.com/564x/14/86/36/148636e35df83740812b9f471d6ae2ea.jpg" target="_blank">Popham Beach State Park</a>: 25 minute drive from Bath</a></div></li>
				<li class="collection-item"><div><a href="http://www.mainegardens.org/" target="_blank">Coastal Maine Botanical Gardens</a></div></li>
				<li class="collection-item"><div>Boothbay Harbor: popular, charming town of coastal Maine. Check out where we got engaged - <a href="http://www.boothbayharbor.com/Members/Play.aspx" target="_blank">Barrett Park!</a></div></li>
				<li class="collection-item"><div>Wiscasset: try the famous <a href="http://www.redseatsmaine.com/" target="_blank">Red's Eats Lobster Roll</a></div></li>
				<li class="collection-item"><div>Pemaquid Lighthouse: Michael's favorite lighthouse in Maine</div></li>
				<li class="collection-item"><div>Acadia National Park: about 2.5 hours north of Bath, but if you have the extra time, definitely <a href="http://multifiles.pressherald.com/uploads/sites/4/2015/06/AP100224043016-1024x683.jpg" target="_blank">worth it</a></div></li>
			  </ul>				  
			  
